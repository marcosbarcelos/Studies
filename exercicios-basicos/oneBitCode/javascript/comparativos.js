const velocity = 20;
const spaceship = 'elemental';

//comparação de valor - retorna true
console.log(velocity == '20');

//comparação de valor e tipo - retorna false
console.log(velocity === '20');

//diferente de valor - retorna true
console.log(spaceship != 'zeus')

//diferente de valor e tipo - retorna true
console.log(spaceship !== 10)

//maior que - retorna true
console.log(velocity > 10)

//maior ou igual que - retorna true
console.log(velocity >= 10)

//menor que - retornna false
console.log(velocity < 20)

//menor ou igual que - retorna true
console.log(velocity <= 20)