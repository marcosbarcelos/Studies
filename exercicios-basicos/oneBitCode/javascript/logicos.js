const velocity = 20;
const maxVelocity = 200;

//operador and - true caso todos satisfaçam
console.log(velocity == 20 && maxVelocity == 200)

//operador or - true caso pelo menos um satisfaça
console.log(velocity == 20 || maxVelocity == 210)

//operador not - inverte os valores true caso false e false caso true
console.log(!(velocity == 20 && maxVelocity == 200))
